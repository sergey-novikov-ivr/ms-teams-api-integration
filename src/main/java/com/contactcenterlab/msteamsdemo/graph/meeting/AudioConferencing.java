package com.contactcenterlab.msteamsdemo.graph.meeting;

public class AudioConferencing {
	
    private String conferenceId;
    private String tollNumber;
    private String tollFreeNumber;
    private String dialinUrl;

	public String getConferenceId() {
		return conferenceId;
	}

	public void setConferenceId(String conferenceId) {
		this.conferenceId = conferenceId;
	}

	public String getTollNumber() {
		return tollNumber;
	}

	public void setTollNumber(String tollNumber) {
		this.tollNumber = tollNumber;
	}

	public String getTollFreeNumber() {
		return tollFreeNumber;
	}

	public void setTollFreeNumber(String tollFreeNumber) {
		this.tollFreeNumber = tollFreeNumber;
	}

	public String getDialinUrl() {
		return dialinUrl;
	}

	public void setDialinUrl(String dialinUrl) {
		this.dialinUrl = dialinUrl;
	}
	
	
}
